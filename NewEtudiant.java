    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
       try
       
       {
           //deleting Student from system
        
        int etudiantId = Integer.valueOf(txtid.getText());
        
        Etudiant etudiant=  new Etudiant();
        if(txtid.getText().trim().equals(""))
            {
                JOptionPane.showMessageDialog(null,  "Enter the Student Reference", "reference Empty", 2);
            }
        else{
            //information
            int yes_or_no= JOptionPane.showConfirmDialog(null, "Do you want to delete this Student?", "Delete Student", JOptionPane.YES_NO_OPTION);
            if (yes_or_no== JOptionPane.YES_OPTION)
                {
                    if(etudiant.DeleteEtudiant(etudiantId))
                        {
                            JOptionPane.showMessageDialog(null, "Student data deleted", " Delete Student",1);
                        }
                        else
                            {
                               JOptionPane.showMessageDialog(null, "Failed", "Delete Student", 2);
                            }
                }
            }
       }catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null,ex.getMessage()+"Enter a valid Student reference", "Invalid reference", 0);
            }
        
    
    }//GEN-LAST:event_deleteActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
       
        //add new etudiant
        
        //get the etudiant data
        
        
        String firstname = txtfirstname.getText();
        String name = txtname.getText();
        String birth = txtbirth.getText();
        String adress = txtadress.getText();
        String departement= txtdep.getText();
        String mail = txtmail.getText();
        //check if data are empty
        //all requiered data 
        Etudiant etudiant = new Etudiant();
        
        if(firstname.trim().equals("") || name.trim().equals("")|| birth.trim().equals("")|| adress.trim().equals("")|| departement.trim().equals("")|| mail.trim().equals(""))
            {
                JOptionPane.showMessageDialog(null,"enter the require info " , "add", 2);
            }
        else{
            if(etudiant.addNewEtudiante(new Etudiant(0,firstname,name,birth,adress,departement,mail)))
             {
               JOptionPane.showMessageDialog(null,"new add" , "add", 1);
            }
             else
            {
             JOptionPane.showMessageDialog(null,"not add" , "add", 2);
            }
        }
      
        
        
    }//GEN-LAST:event_addActionPerformed

