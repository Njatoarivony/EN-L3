    //create a function to move all data to the arraylist
    public void StudentsData()
    {
        
        Etudiant etudiant= new Etudiant(); 
        ArrayList<Etudiant> Studentlist= etudiant.Studentlist();
        //TABLE COLUMNS
        String[] colNames= {"Reference","Firstname","Name","Birth"," Adress","Departement","Mail"};
        //TABLE ROW
        Object[][] rows= new Object[Studentlist.size()][7];
        for(int i=0 ;i< Studentlist.size(); i++)
        {
            rows[i][0]= Studentlist.get(i).getId();
            rows[i][1]= Studentlist.get(i).getFirstname();
            rows[i][2]= Studentlist.get(i).getName();
            rows[i][3]= Studentlist.get(i).getBirth();
            rows[i][4]= Studentlist.get(i).getAdress();
            rows[i][5]= Studentlist.get(i).getDepartement();
            rows[i][6]= Studentlist.get(i).getMail();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable1.setModel(model);
        jTable1.setRowHeight(40);
    }

